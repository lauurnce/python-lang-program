num = int(input("Enter a number between 1 and 10: "))

# Check the number and print the matching word
if num == 1:
    print("One")
elif num == 2:
    print("Two")
elif num == 3:
    print("Three")
elif num == 4:
    print("Four")
elif num == 5:
    print("Five")
elif num == 6:
    print("Six")
elif num == 7:
    print("Seven")
elif num == 8:
    print("Eight")
elif num == 9:
    print("Nine")
elif num == 10:
    print("Ten")
else:
    # If the number is not between 1 and 10, print an error
    print("Number is out of range. Please enter 1-10.")