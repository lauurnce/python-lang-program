
# While loop with an else statement 
count = 0 # Initialize a counter variable 
while (count < 3): # Check if count is less than 3 
    count = count + 1 # Increase count by 1 each loop 
    print("Hello Geek") 
else: # Triggers when count is no longer less than 3 
    print("In Else Block") 

# For loop iterating over a sequence (list)
list = ["geeks", "for", "geeks"] # Create a list 
for index in range(len(list)): # Iterate using the length of the list 
    print (list[index]) # Print the item at the current index 
